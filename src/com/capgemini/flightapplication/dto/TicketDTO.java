package com.capgemini.flightapplication.dto;

import java.util.Date;

import org.springframework.stereotype.Component;
@Component
public class TicketDTO 
{
	private int bookId;
	private Date dateOfJourney;
	private String classOfTravel;
	private int noOfAdults;
	private int noOfChild;
	private String coupon;
	private float ticketPrice;
	private String ticketStatus;
	private String source;
	private String destination;
	private FlightDTO flightObj;
	private CustomerDTO customerObj;
	public String getSource() {
		return source;
	}
	public void setSource(String source) {
		this.source = source;
	}
	public String getDestination() {
		return destination;
	}
	public void setDestination(String destination) {
		this.destination = destination;
	}
	public int getBookId() {
		return bookId;
	}
	public void setBookId(int bookId) {
		this.bookId = bookId;
	}

	public Date getDateOfJourney() {
		return dateOfJourney;
	}
	public void setDateOfJourney(Date dateOfJourney) {
		this.dateOfJourney = dateOfJourney;
	}
	public String getClassOfTravel() {
		return classOfTravel;
	}
	public void setClassOfTravel(String classOfTravel) {
		this.classOfTravel = classOfTravel;
	}
	public int getNoOfAdults() {
		return noOfAdults;
	}
	public void setNoOfAdults(int noOfAdults) {
		this.noOfAdults = noOfAdults;
	}
	public int getNoOfChild() {
		return noOfChild;
	}
	public void setNoOfChild(int noOfChild) {
		this.noOfChild = noOfChild;
	}
	public String getCoupon() {
		return coupon;
	}
	public void setCoupon(String coupon) {
		this.coupon = coupon;
	}
	public float getTicketPrice() {
		return ticketPrice;
	}
	public void setTicketPrice(float ticketPrice) {
		this.ticketPrice = ticketPrice;
	}
	public String getTicketStatus() {
		return ticketStatus;
	}
	public void setTicketStatus(String ticketStatus) {
		this.ticketStatus = ticketStatus;
	}
	public FlightDTO getFlightObj() {
		return flightObj;
	}
	public void setFlightObj(FlightDTO flightObj) {
		this.flightObj = flightObj;
	}
	public CustomerDTO getCustomerObj() {
		return customerObj;
	}
	public void setCustomerObj(CustomerDTO customerObj) {
		this.customerObj = customerObj;
	}
}
